sprites.onOverlap(SpriteKind.Player, SpriteKind.spikes, function (sprite, otherSprite) {
    cat.vy = -100
    info.changeLifeBy(-1)
})
sprites.onOverlap(SpriteKind.Player, SpriteKind.Trap, function (sprite, otherSprite) {
    take_damage()
})
sprites.onOverlap(SpriteKind.Player, SpriteKind.Coin, function (sprite, otherSprite) {
    animation.runMovementAnimation(
    otherSprite,
    animation.animationPresets(animation.easeUp),
    1000,
    false
    )
    info.changeScoreBy(1)
    pause(500)
    otherSprite.destroy()
})
function launch () {
    if (7 < charge) {
        charge = 0
        x_speed = 200
        cat.vy = -190
    } else {
        cat.vy = -150
    }
}
// プレイヤー(ネコ)がでぐちについたら
sprites.onOverlap(SpriteKind.Player, SpriteKind.Portal, function (sprite, otherSprite) {
    // レベルに1をたす
    current_level += 1
    if (current_level == levels.length) {
        // マップのはいれつ
        // おわったらゴール
        game.over(true)
    }
    start_level()
})
function stop () {
    cat.vx = 0
}
// ダメージをうけた
// ライフを1へらして
// ネコをスタートいちにもどす
function take_damage () {
    info.changeLifeBy(-1)
    if (0 < info.life()) {
        scene.placeOnRandomTile(cat, 3)
    }
}
scene.onHitTile(SpriteKind.Player, 0, function (sprite) {
    if (sprite.isHittingTile(CollisionDirection.Bottom)) {
        take_damage()
    }
})
